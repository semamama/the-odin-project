// a function that capitalizes ONLY the first letter of the strung
function capitalize(string) {
  return string[0].toUpperCase() + string.slice(1);
}

// function to grab the LAST letter
function lastLetter(str) {
  let lastL = str.length - 1;
  return str[lastL];
}
